    @Override
    public void paintComponent(Graphics g) {
        super.paintComponent(g);
        g.setColor(Color.red);
        g.drawRect(12, 25, 100, 20);
        g.setColor(Color.blue);
        g.fillRect(12, 50, 100, 20);
        g.drawString("Hello World!", 12, 100);
        g.setColor(Color.black);
        g.drawRoundRect(12, 120, 50, 50, 10, 10);
        g.fillRoundRect(12, 200, 50, 50, 30, 30);
        g.drawOval(100, 10, 40, 60);
        g.drawLine(100, 70, 150, 100);
        g.drawArc(100, 150, 50, 50, 370, -270);
        g.fillArc(100, 220, 50, 50, 370, -270);
        
        Polygon p = new Polygon();
        Random r = new Random();
        for (int i = 0; i < 50; i++) {
            p.addPoint(200 + r.nextInt(100), 100 + r.nextInt(100));
        }
        
        g.drawPolygon(p);
        
        
    }

