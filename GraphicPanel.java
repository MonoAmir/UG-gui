    @Override
    public void actionPerformed(ActionEvent e) {
        Color c = Color.yellow;
        c = JColorChooser.showDialog(this, ":)", c);
        if (c != null) {
            b.setBackground(c);
            lr.setOpaque(true);
            lg.setOpaque(true);
            lb.setOpaque(true);
            lr.setBackground(new Color(c.getRed(), c.getRed(), c.getRed()));
            lg.setBackground(new Color(c.getGreen(), c.getGreen(), c.getGreen()));
            lb.setBackground(new Color(c.getBlue(), c.getBlue(), c.getBlue()));
        }
    }
